public class Discuss {
    String lessonNum;//课程编号
    String lessonName;
    String recDiscussNum;//回复编号
    String recDiscussName;
    String talkUserNum;//发言编号
    String talkUserName;
    String discussContent;

    public String getLessonNum() {
        return lessonNum;
    }

    public void setLessonNum(String lessonNum) {
        this.lessonNum = lessonNum;
    }

    public String getLessonName() {
        return lessonName;
    }

    public void setLessonName(String lessonName) {
        this.lessonName = lessonName;
    }

    public String getRecDiscussNum() {
        return recDiscussNum;
    }

    public void setRecDiscussNum(String recDiscussNum) {
        this.recDiscussNum = recDiscussNum;
    }

    public String getRecDiscussName() {
        return recDiscussName;
    }

    public void setRecDiscussName(String recDiscussName) {
        this.recDiscussName = recDiscussName;
    }

    public String getTalkUserNum() {
        return talkUserNum;
    }

    public void setTalkUserNum(String talkUserNum) {
        this.talkUserNum = talkUserNum;
    }

    public String getDiscussContent() {
        return discussContent;
    }

    public void setDiscussContent(String discussContent) {
        this.discussContent = discussContent;
    }

    public String getTalkUserName() {
        return talkUserName;
    }

    public void setTalkUserName(String talkUserName) {
        this.talkUserName = talkUserName;
    }

}
